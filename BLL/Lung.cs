        public bool SaveOrUpdate(Model.Lung o)
        {
            List<Maticsoft.Model.Lung> trList = GetModelList("PID=" + o.PID.ToString() + " And Period=" + o.Period.ToString());
            if (trList != null && trList.Count > 0)
            {
                Maticsoft.Model.Lung oo = trList[0];
                o.ID = oo.ID;
                return Update(o);

